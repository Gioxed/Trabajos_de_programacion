// Convertir un número entero a una cadena de texto
let numero = 42;
let numeroComoTexto = numero.toString();
console.log("Número convertido a texto: " + numeroComoTexto);
console.log("Tipo de dato: " + typeof numeroComoTexto);

// Convertir una cadena de texto a un número entero
let texto = "123";
let textoComoNumero = parseInt(texto);
console.log("Texto convertido a número: " + textoComoNumero);
console.log("Tipo de dato: " + typeof textoComoNumero);
